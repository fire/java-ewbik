package extensionExamples;

/**
 * 
 * extremely basic implementation of a Vector 
 * object to serve as an example for how to 
 * extend this library
 * 
 * @author Eron Gjoni
 *
 */

public class ExampleVector {

	public double x, y, z; 
	
	public ExampleVector(double x, double y, double z) {
		this.x = x;
		this.z = z; 
		this.y = y;
	}
	
}
